package homework;

public class Drink {
	//속성(멤버변수들)
	String name;
	int price;
	
	
	//동작(멤버메소드)
	public void setName(String newName) {
		//인자로 받은 newName을 멤버변수 name에 저장
		name = newName;
	}
	
	public void setPrice(int newPrice) {
		//인자로 받은 newPrice을 멤버변수 price에 저장
		price = newPrice;
	}
	
	
	

	
}
